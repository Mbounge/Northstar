export class NorthstarTurnValidationError extends TypeError {
  readonly code: string;

  constructor(code: string, message: string) {
    super(message);
    this.name = "NorthstarTurnValidationError";
    this.code = code;
  }
}

function fail(code: string, message: string): never {
  throw new NorthstarTurnValidationError(code, message);
}

function structuralJSON(value: unknown): string {
  return JSON.stringify(value);
}

const FORBIDDEN_MODEL_IDENTITY_KEYS = new Set([
  "runId",
  "taskId",
  "attemptId",
  "commitHash",
  "candidateCommitHash",
  "candidateStateHash",
  "projectionReceipt",
  "taskSequence",
  "attemptNumber",
  "commitSequence",
]);

export function assertNoNorthstarSystemIdentity(
  value: NorthstarLedgerValue,
  path = "$",
): void {
  if (Array.isArray(value)) {
    value.forEach((entry, index) => assertNoNorthstarSystemIdentity(entry, `${path}[${index}]`));
    return;
  }
  if (value !== null && typeof value === "object") {
    for (const [key, entry] of Object.entries(value)) {
      if (FORBIDDEN_MODEL_IDENTITY_KEYS.has(key)) {
        fail("MODEL_SYSTEM_IDENTITY_FORBIDDEN", `${path}.${key} is owned by the ledger and cannot be generated by the model.`);
      }
      assertNoNorthstarSystemIdentity(entry, `${path}.${key}`);
    }
  }
}

function record(value: unknown, path: string): Record<string, unknown> {
  if (value === null || typeof value !== "object" || Array.isArray(value)) {
    return fail("INVALID_OBJECT", `${path} must be an object.`);
  }
  const prototype = Object.getPrototypeOf(value);
  if (prototype !== Object.prototype && prototype !== null) {
    return fail("INVALID_OBJECT", `${path} must be a plain object.`);
  }
  return value as Record<string, unknown>;
}

function exactKeys(
  value: Record<string, unknown>,
  required: readonly string[],
  optional: readonly string[],
  path: string,
): void {
  const requiredSet = new Set(required);
  const allowed = new Set([...required, ...optional]);
  for (const key of required) {
    if (!Object.prototype.hasOwnProperty.call(value, key)) {
      fail("MISSING_FIELD", `${path}.${key} is required.`);
    }
  }
  for (const key of Object.keys(value)) {
    if (!allowed.has(key)) fail("UNKNOWN_FIELD", `${path}.${key} is not allowed.`);
  }
  for (const key of requiredSet) {
    if (value[key] === undefined) fail("MISSING_FIELD", `${path}.${key} is required.`);
  }
}

function stringValue(value: unknown, path: string, options?: { min?: number; max?: number }): string {
  if (typeof value !== "string") fail("INVALID_STRING", `${path} must be a string.`);
  const min = options?.min ?? 1;
  const max = options?.max ?? 100_000;
  if (value.length < min || value.length > max) {
    fail("INVALID_STRING_LENGTH", `${path} must contain between ${min} and ${max} characters.`);
  }
  return value;
}

function optionalString(value: unknown, path: string): string | undefined {
  if (value === undefined) return undefined;
  return stringValue(value, path);
}

function finiteNumber(value: unknown, path: string, options?: { integer?: boolean; min?: number; max?: number }): number {
  if (typeof value !== "number" || !Number.isFinite(value)) {
    fail("INVALID_NUMBER", `${path} must be a finite number.`);
  }
  if (options?.integer && !Number.isInteger(value)) fail("INVALID_NUMBER", `${path} must be an integer.`);
  if (options?.min !== undefined && value < options.min) fail("INVALID_NUMBER", `${path} must be at least ${options.min}.`);
  if (options?.max !== undefined && value > options.max) fail("INVALID_NUMBER", `${path} must be at most ${options.max}.`);
  return Object.is(value, -0) ? 0 : value;
}

function booleanValue(value: unknown, path: string): boolean {
  if (typeof value !== "boolean") fail("INVALID_BOOLEAN", `${path} must be a boolean.`);
  return value;
}

function enumValue<T extends string>(value: unknown, choices: readonly T[], path: string): T {
  if (typeof value !== "string" || !choices.includes(value as T)) {
    fail("INVALID_ENUM", `${path} must be one of ${choices.join(", ")}.`);
  }
  return value as T;
}

function ledgerValue(value: unknown, path: string): NorthstarLedgerValue {
  try {
    assertValidNorthstarLedgerValue(value, path);
    return cloneNorthstarLedgerValue(value);
  } catch (error) {
    fail(
      "INVALID_LEDGER_VALUE",
      error instanceof Error ? error.message : `${path} is not a valid ledger value.`,
    );
  }
}

function optionalLedgerValue(value: unknown, path: string): NorthstarLedgerValue | undefined {
  return value === undefined ? undefined : ledgerValue(value, path);
}

function arrayValue<T>(value: unknown, path: string, parser: (entry: unknown, entryPath: string) => T): T[] {
  if (!Array.isArray(value)) fail("INVALID_ARRAY", `${path} must be an array.`);
  return value.map((entry, index) => parser(entry, `${path}[${index}]`));
}

function parseFailure(value: unknown, path: string): NorthstarLedgerFailure {
  const input = record(value, path);
  exactKeys(input, ["kind", "code", "detail", "phase"], ["correctionContext"], path);
  return {
    kind: enumValue(input.kind, ["transient", "correctable", "terminal"] as const, `${path}.kind`),
    code: stringValue(input.code, `${path}.code`, { max: 200 }),
    detail: stringValue(input.detail, `${path}.detail`, { max: 20_000 }),
    phase: enumValue(
      input.phase,
      ["decision", "execution", "preparation", "projection", "control"] as const,
      `${path}.phase`,
    ),
    correctionContext: optionalLedgerValue(input.correctionContext, `${path}.correctionContext`),
  };
}

function parseProjectionReceipt(value: unknown, path: string): NorthstarProjectionReceipt {
  const input = record(value, path);
  exactKeys(
    input,
    ["commitHash", "projectedStateHash", "surfaceSessionId", "verified", "projectedAt"],
    ["metadata"],
    path,
  );
  return {
    commitHash: stringValue(input.commitHash, `${path}.commitHash`, { max: 256 }),
    projectedStateHash: stringValue(input.projectedStateHash, `${path}.projectedStateHash`, { max: 256 }),
    surfaceSessionId: stringValue(input.surfaceSessionId, `${path}.surfaceSessionId`, { max: 256 }),
    verified: booleanValue(input.verified, `${path}.verified`),
    projectedAt: finiteNumber(input.projectedAt, `${path}.projectedAt`),
    metadata: optionalLedgerValue(input.metadata, `${path}.metadata`),
  };
}

const TASK_KINDS = ["research", "analysis", "artboard-mutation", "verification", "finalization"] as const;
const TASK_STATUSES = [
  "created",
  "active",
  "awaiting-preparation",
  "awaiting-projection",
  "awaiting-transport-resolution",
  "retryable-failure",
  "blocked",
  "completed",
  "cancelled",
  "superseded",
] as const;
const ATTEMPT_STATUSES = ["active", "transport-uncertain", "drafted", "prepared", "failed", "cancelled", "completed"] as const;

function parseTask(value: unknown, path: string): NorthstarLedgerTask {
  const input = record(value, path);
  exactKeys(
    input,
    [
      "id",
      "runId",
      "sequence",
      "kind",
      "intent",
      "expectedOutcome",
      "initialExecutionInput",
      "status",
      "baseCommitHash",
      "currentAttemptId",
      "createdAt",
    ],
    ["resultCommitHash", "completedAt", "cancelledAt", "supersededAt"],
    path,
  );
  return {
    id: stringValue(input.id, `${path}.id`, { max: 256 }),
    runId: stringValue(input.runId, `${path}.runId`, { max: 256 }),
    sequence: finiteNumber(input.sequence, `${path}.sequence`, { integer: true, min: 1 }),
    kind: enumValue(input.kind, TASK_KINDS, `${path}.kind`),
    intent: stringValue(input.intent, `${path}.intent`, { max: 20_000 }),
    expectedOutcome: stringValue(input.expectedOutcome, `${path}.expectedOutcome`, { max: 20_000 }),
    initialExecutionInput: ledgerValue(input.initialExecutionInput, `${path}.initialExecutionInput`),
    status: enumValue(input.status, TASK_STATUSES, `${path}.status`),
    baseCommitHash: stringValue(input.baseCommitHash, `${path}.baseCommitHash`, { max: 256 }),
    currentAttemptId: input.currentAttemptId === null
      ? null
      : stringValue(input.currentAttemptId, `${path}.currentAttemptId`, { max: 256 }),
    resultCommitHash: optionalString(input.resultCommitHash, `${path}.resultCommitHash`),
    createdAt: finiteNumber(input.createdAt, `${path}.createdAt`),
    completedAt: input.completedAt === undefined ? undefined : finiteNumber(input.completedAt, `${path}.completedAt`),
    cancelledAt: input.cancelledAt === undefined ? undefined : finiteNumber(input.cancelledAt, `${path}.cancelledAt`),
    supersededAt: input.supersededAt === undefined ? undefined : finiteNumber(input.supersededAt, `${path}.supersededAt`),
  };
}

